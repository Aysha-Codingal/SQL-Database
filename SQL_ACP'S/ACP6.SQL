CREATE TABLE IF NOT EXISTS PRODUCTS (
    PRODUCT_ID INTEGER PRIMARY KEY,
    NAME TEXT NOT NULL,
    CATEGORY TEXT,
    EXPORT_COUNTRY TEXT,
    PRICE REAL
);


